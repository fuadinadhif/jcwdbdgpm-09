/* ----------------------------- Loose equal == ----------------------------- */
// Untuk membandingkan apakah kedua value itu setara/sama
console.log(10 == 10);
console.log(5 == 5);
console.log(1 == "ABC");
console.log(1 == "1");

/* ---------------------------- Strict equal === ---------------------------- */
console.log(1 === "1");

/* ----------------------------- Greater than > ----------------------------- */
console.log(10 > 5);
console.log(10 > 5000);
console.log(10 > 10);

/* -------------------------- Greater than or equal ------------------------- */
console.log(10 >= 10);

// Less than <
// Less than or equal <=

/* ------------------------------ Not equal !== ----------------------------- */
console.log(10 !== 10);
console.log(10 !== 1);

